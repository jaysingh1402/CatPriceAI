"""## 8. PricingAgent Class

"""

class PricingAgent:
    """ReAct-style agent that reasons across all CatPriceAI modules.
    Uses LangChain ReAct (OpenAI) when OPENAI_API_KEY is set;
    otherwise falls back to a keyword router (offline, still fully functional).
    All data read from Google Drive."""

    def __init__(self):
        self.tools = build_langchain_tools()
        self.agent = self._build_agent()
        mode = 'ReAct (OpenAI)' if self.agent else 'Simple Router (offline)'
        print(f'🤖 PricingAgent ready — {mode} mode.')

    def _build_agent(self):
        if not USE_OPENAI:
            return None
        try:
            from langchain_openai import ChatOpenAI
            from langchain.agents import create_react_agent, AgentExecutor
            from langchain import hub
            llm    = ChatOpenAI(model='gpt-4o-mini', temperature=0)
            prompt = hub.pull('hwchase17/react')
            agent  = create_react_agent(llm=llm, tools=self.tools, prompt=prompt)
            return AgentExecutor(agent=agent, tools=self.tools, verbose=True, max_iterations=6)
        except Exception as e:
            print(f'⚠️ ReAct build failed: {e}. Using keyword router.')
            return None

    def run(self, question: str) -> str:
        if self.agent is not None:
            try:
                result = self.agent.invoke({'input': question})
                return result.get('output', str(result))
            except Exception as e:
                return f'Agent error: {e}'
        return self._simple_router(question)

    def _simple_router(self, question: str) -> str:
        """Keyword-based routing — offline fallback."""

        if any(kw in question for kw in ['exposure', 'portfolio', 'tiv', 'location', 'coast',
                                    'flood zone', 'construction', 'wind', 'concentration']):
            return f'EXPOSURE SUMMARY:\n{tool_get_exposure_summary()}'

        elif any(kw in question for kw in ['burning', 'historical', 'loss history', 'aal',
                                      'experience', 'penetration', 'empirical']):
            return f'BURNING COST:\n{tool_run_burning_cost()}'

        elif any(kw in question for kw in ['market', 'benchmark', 'industry', 'competitive',
                                      'compare', 'peers']):
            return f'MARKET BENCHMARKS:\n{tool_get_market_benchmarks()}'

        elif "should we quote" in question or any(kw in question.lower() for kw in ['quote this treaty', 'should we quote']):
            return f'TREATY QUOTE GUIDANCE:\n{tool_query_treaty_terms("Should we quote this treaty? What are the quoting conditions, eligibility criteria, and any clauses relevant to underwriting or declining this treaty?")}'

        elif any(kw in question for kw in ['price', 'rol', 'premium', 'technical', 'recommend',
                                      'quote', 'adequate', 'adequacy', 'ml', 'model',
                                      'blended', 'freq', 'severity']):
            return f'TECHNICAL PRICE:\n{tool_get_technical_price()}'

        elif any(kw in question for kw in ['treaty', 'clause', 'coverage', 'exclusion',
                                      'attachment', 'reinstate', 'peril', 'definition',
                                      'eligib', 'mdr', 'claim', 'wording', 'excluded']):
            return f'TREATY Q&A:\n{tool_query_treaty_terms(question)}'

        else:
            return (
                "I am a pricing agent for the reinsurance treaty and cannot answer questions "
                "unrelated to treaty pricing. I can help with: exposure summary, burning cost, "
                "technical price / ROL, market benchmarks, treaty terms and clauses, or quoting guidance."
            )

print('✅ PricingAgent class defined.')

