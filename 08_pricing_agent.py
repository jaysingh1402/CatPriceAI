# ── Tool 4: Market Benchmarks ─────────────────────────────────
def tool_get_market_benchmarks() -> str:
    """Returns Gulf Coast market ROL benchmarks from Drive data/market_rol.csv."""
    data = _load_csv(MARKET_CSV)
    if data is None:
        return f'❌ Not found: {MARKET_CSV}'
    mkt    = pd.read_csv(MARKET_CSV)
    recent = mkt[(mkt['year'] >= 2021) & (mkt['region'] == 'Gulf Coast')]
    by_peril = (
        recent.groupby('peril')['market_rol']
              .agg(['mean', 'min', 'max'])
              .reset_index()
              .rename(columns={'mean': 'avg_rol', 'min': 'min_rol', 'max': 'max_rol'})
    )
    by_peril[['avg_rol', 'min_rol', 'max_rol']] = (
        by_peril[['avg_rol', 'min_rol', 'max_rol']] * 100
    ).round(2)
    return by_peril.to_json(orient='records', indent=2)


