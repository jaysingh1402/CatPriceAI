# ── BM25 Retriever class (must match Student 7 definition) ───
class BM25Retriever:
    def __init__(self, chunks):
        self.chunks = chunks
        corpus = [chunk['content'].lower().split() for chunk in chunks]
        self.bm25 = BM25Okapi(corpus)

    def search(self, query, k=10):
        query_tokens = query.lower().split()
        scores = self.bm25.get_scores(query_tokens)
        ranked = sorted(
            zip(self.chunks, scores),
            key=lambda x: x[1], reverse=True
        )
        return ranked[:k]


# ── Custom unpickler — same as Student 7 ─────────────────────
class CustomUnpickler(pickle.Unpickler):
    def find_class(self, module, name):
        if module == 'bm25_engine' and name == 'BM25Retriever':
            return BM25Retriever
        return super().find_class(module, name)


# ── Groq LLM wrapper ─────────────────────────────────────────
def generate_answer(prompt: str) -> str:
    client = Groq(api_key=GROQ_API_KEY)
    response = client.chat.completions.create(
        model='llama-3.3-70b-versatile',
        messages=[{'role': 'user', 'content': prompt}],
        temperature=0,
        max_tokens=1000
    )
    return response.choices[0].message.content


# ── Hybrid Retriever (FAISS + BM25 Reciprocal Rank Fusion) ───
class HybridRetriever:
    def __init__(self, faiss_vectorstore, bm25_retriever):
        self.faiss = faiss_vectorstore
        self.bm25  = bm25_retriever

    def search(self, query, faiss_k=20, bm25_k=20, final_k=10):
        combined = {}
        for rank, (doc, score) in enumerate(
            self.faiss.similarity_search_with_score(query, k=faiss_k)
        ):
            key = doc.page_content
            combined.setdefault(key, {'doc': doc, 'score': 0})
            combined[key]['score'] += 1.0 / (rank + 1)

        for rank, (chunk, score) in enumerate(self.bm25.search(query, k=bm25_k)):
            key = chunk['content']
            combined.setdefault(key, {'doc': None, 'score': 0})
            combined[key]['score'] += 1.0 / (rank + 1)

        ranked = sorted(combined.values(), key=lambda x: x['score'], reverse=True)
        return [item['doc'] for item in ranked if item['doc'] is not None][:final_k]


# ── RAG Engine ────────────────────────────────────────────────
class RagEngine:
    def __init__(self, retriever):
        self.retriever = retriever

    def ask(self, question: str) -> str:
        docs = self.retriever.search(query=question, final_k=10)
        context = '\n\n'.join(doc.page_content for doc in docs)
        prompt = f"""
You are a Reinsurance Treaty Analysis Assistant.

Instructions:
1. Answer ONLY from the provided context.
2. Do NOT use outside knowledge.
3. If the answer is not found, say: "I could not find the answer in the provided documents."
4. Mention the relevant Article or Section if available.
5. Be concise and professional.

CONTEXT:
{context}

QUESTION:
{question}

ANSWER:"""
        return generate_answer(prompt)


